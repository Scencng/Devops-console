export function useI18n() {
  const localeStore = useLocaleStore()

  const locale = computed(() => localeStore.locale)

  function t(key: string, params?: MessageParams) {
    const current = messages[localeStore.locale] as Record<string, string>
    const fallback = messages['zh-CN'] as Record<string, string>
    const currentRuntimeFallback = runtimeFallbackMessages[localeStore.locale] ?? {}
    const zhRuntimeFallback = runtimeFallbackMessages['zh-CN'] ?? {}
    let message = current[key] ?? currentRuntimeFallback[key] ?? fallback[key] ?? zhRuntimeFallback[key] ?? key

    if (params) {
      for (const [name, value] of Object.entries(params)) {
        message = message.replace(new RegExp(`\\{${name}\\}`, 'g'), String(value))
      }
    }

    return message.replace(/[。.]+$/u, '')
  }

  return {
    locale,
    isChinese: computed(() => localeStore.isChinese),
    toggleLocale: localeStore.toggleLocale,
    setLocale: localeStore.setLocale,
    t
  }
}
